package Testujemy;

public class Squash {

    String amen;
    String mamen;
    int min;

    public Squash(String amen, String mamen, int min) {
        this.amen = amen;
        this.mamen = mamen;
        this.min = min;
    }

    public String getAmen() {
        return amen;
    }

    public String getMamen() {
        return mamen;
    }

    public int getMin() {
        return min;
    }

    public void setAmen(String amen) {
        this.amen = amen;
    }
}
